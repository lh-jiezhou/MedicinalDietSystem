package com.lh.pojo;


public class AdminSysTable {

  private long adminSysId;
  private String adminSysNo;
  private String adminSysPwd;
  private String adminSysTel;
  private String adminSysName;


  public long getAdminSysId() {
    return adminSysId;
  }

  public void setAdminSysId(long adminSysId) {
    this.adminSysId = adminSysId;
  }


  public String getAdminSysNo() {
    return adminSysNo;
  }

  public void setAdminSysNo(String adminSysNo) {
    this.adminSysNo = adminSysNo;
  }


  public String getAdminSysPwd() {
    return adminSysPwd;
  }

  public void setAdminSysPwd(String adminSysPwd) {
    this.adminSysPwd = adminSysPwd;
  }


  public String getAdminSysTel() {
    return adminSysTel;
  }

  public void setAdminSysTel(String adminSysTel) {
    this.adminSysTel = adminSysTel;
  }


  public String getAdminSysName() {
    return adminSysName;
  }

  public void setAdminSysName(String adminSysName) {
    this.adminSysName = adminSysName;
  }

}
