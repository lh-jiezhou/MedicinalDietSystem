package com.lh.pojo;


public class ApplicationConnTable {

  private long applicationConnId;
  private long applicationId;
  private long goodsId;
  private long goodsCategory;


  public long getApplicationConnId() {
    return applicationConnId;
  }

  public void setApplicationConnId(long applicationConnId) {
    this.applicationConnId = applicationConnId;
  }


  public long getApplicationId() {
    return applicationId;
  }

  public void setApplicationId(long applicationId) {
    this.applicationId = applicationId;
  }


  public long getGoodsId() {
    return goodsId;
  }

  public void setGoodsId(long goodsId) {
    this.goodsId = goodsId;
  }


  public long getGoodsCategory() {
    return goodsCategory;
  }

  public void setGoodsCategory(long goodsCategory) {
    this.goodsCategory = goodsCategory;
  }

}
