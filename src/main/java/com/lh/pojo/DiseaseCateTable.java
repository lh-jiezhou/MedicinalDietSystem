package com.lh.pojo;


public class DiseaseCateTable {

  private long diseaseId;
  private String diseaseName;
  private String diseaseInformation;
  private String diseaseSuitable;
  private String diseaseTaboo;


  public long getDiseaseId() {
    return diseaseId;
  }

  public void setDiseaseId(long diseaseId) {
    this.diseaseId = diseaseId;
  }


  public String getDiseaseName() {
    return diseaseName;
  }

  public void setDiseaseName(String diseaseName) {
    this.diseaseName = diseaseName;
  }


  public String getDiseaseInformation() {
    return diseaseInformation;
  }

  public void setDiseaseInformation(String diseaseInformation) {
    this.diseaseInformation = diseaseInformation;
  }


  public String getDiseaseSuitable() {
    return diseaseSuitable;
  }

  public void setDiseaseSuitable(String diseaseSuitable) {
    this.diseaseSuitable = diseaseSuitable;
  }


  public String getDiseaseTaboo() {
    return diseaseTaboo;
  }

  public void setDiseaseTaboo(String diseaseTaboo) {
    this.diseaseTaboo = diseaseTaboo;
  }

}
