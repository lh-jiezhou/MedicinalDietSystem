package com.lh.pojo;


public class EfficacyCateTable {

  private long efficacyId;
  private String efficacyName;
  private String efficacyCrowd;
  private String efficacyInformation;
  private String efficacySuitable;
  private String efficacyTaboo;

  public long getEfficacyId() {
    return efficacyId;
  }

  public void setEfficacyId(long efficacyId) {
    this.efficacyId = efficacyId;
  }


  public String getEfficacyName() {
    return efficacyName;
  }

  public void setEfficacyName(String efficacyName) {
    this.efficacyName = efficacyName;
  }


  public String getEfficacyCrowd() {
    return efficacyCrowd;
  }

  public void setEfficacyCrowd(String efficacyCrowd) {
    this.efficacyCrowd = efficacyCrowd;
  }

  public String getEfficacyInformation() {
    return efficacyInformation;
  }

  public void setEfficacyInformation(String efficacyInformation) {
    this.efficacyInformation = efficacyInformation;
  }

  public String getEfficacySuitable() {
    return efficacySuitable;
  }

  public void setEfficacySuitable(String efficacySuitable) {
    this.efficacySuitable = efficacySuitable;
  }

  public String getEfficacyTaboo() {
    return efficacyTaboo;
  }

  public void setEfficacyTaboo(String efficacyTaboo) {
    this.efficacyTaboo = efficacyTaboo;
  }
}
