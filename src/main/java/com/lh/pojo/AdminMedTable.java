package com.lh.pojo;


public class AdminMedTable {

  private long adminMedId;
  private String adminMedNo;
  private String adminMedPwd;
  private String adminMedTel;
  private String adminMedName;
  private String adminMedImage;


  public long getAdminMedId() {
    return adminMedId;
  }

  public void setAdminMedId(long adminMedId) {
    this.adminMedId = adminMedId;
  }


  public String getAdminMedNo() {
    return adminMedNo;
  }

  public void setAdminMedNo(String adminMedNo) {
    this.adminMedNo = adminMedNo;
  }


  public String getAdminMedPwd() {
    return adminMedPwd;
  }

  public void setAdminMedPwd(String adminMedPwd) {
    this.adminMedPwd = adminMedPwd;
  }


  public String getAdminMedTel() {
    return adminMedTel;
  }

  public void setAdminMedTel(String adminMedTel) {
    this.adminMedTel = adminMedTel;
  }


  public String getAdminMedName() {
    return adminMedName;
  }

  public void setAdminMedName(String adminMedName) {
    this.adminMedName = adminMedName;
  }


  public String getAdminMedImage() {
    return adminMedImage;
  }

  public void setAdminMedImage(String adminMedImage) {
    this.adminMedImage = adminMedImage;
  }

}
